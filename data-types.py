# int
a = 5
b = 3
c = 0
print(type(a))

# float
a = 3.5
b = 2.5
c = a + b 
print(c, "with the type of", type(c))

# str
a = 'Hello'
b = 'World'
print(a , b, "is a type of", type(a))

# bool
# First letter should be capital
a = True
b = False
print(a, b, "is a type of", type(a))

# list 
# enclosed in square brackets []
a = [1, 2, 3, 4]
b = ['apple', 'banana', 'mango']
print(a, b, "is a type of", type(b))

# tuple
# immutable items in parentheses ()
a = (1, 2, 3, 4)
b = ('apple', 'banana', 'cherry')
print(type(b))

# dict
# key:value pair enclosed in curly braces {}
a = {'name': 'John', 'age': 30, 'city': 'New York'}
b = {1: 'one', 2: 'two', 3: 'three'}
print(type(a))